#ifndef STD_TYPES_H_
#define STD_TYPES_H_

//======================================================================================================================
// STANDART TYPES
//======================================================================================================================
typedef unsigned char   boolean;

typedef unsigned char   uint8;
typedef unsigned short  uint16;
typedef unsigned long   uint32;

typedef signed char     sint8;
typedef signed short    sint16;
typedef signed long     sint32;
//----------------------------------------------------------------------------------------------------------------------


//======================================================================================================================
// GLOBAL CONSTANTS
//======================================================================================================================
// bit constants
#define HIGH            ((uint8)1)
#define LOW             ((uint8)0)
#define ONE             ((uint8)1)
#define ZERO            ((uint8)0)
#define INPUT           ((uint8)1)
#define OUTPUT          ((uint8)0)
//----------------------------------------------------------------------------------------------------------------------
// boolean type and constants
#ifdef _WIN32
#   undef   FALSE
#   define  FALSE       ((boolean)0)
#   undef   TRUE
#   define  TRUE        (!FALSE)
#else
#   undef   TRUE
#   define  TRUE        ((boolean)(1==1))
#   undef   FALSE
#   define  FALSE       ((boolean)(!TRUE))
#endif
//----------------------------------------------------------------------------------------------------------------------
// NULL pointer definition
#ifndef NULL
#   ifdef __cplusplus
#       define NULL     0
#   else
#       define NULL     ((void *)0)
#   endif
#endif
//----------------------------------------------------------------------------------------------------------------------

//======================================================================================================================
// STANDART RETURN TYPE
//======================================================================================================================
typedef uint8 ResultType;
#define RES_OK          ((uint8)0)
#define RES_WAIT        ((uint8)1)
#define RES_INV_ARG     ((uint8)(-1))
#define RES_ERROR       ((uint8)(-2))

//----------------------------------------------------------------------------------------------------------------------

//======================================================================================================================
// GLOBAL MACROSSES
//======================================================================================================================
#define RESET(aValue)           ((aValue) = 0)
//----------------------------------------------------------------------------------------------------------------------
#define INVERT(aValue)          ((aValue) = (!(aValue)))
//----------------------------------------------------------------------------------------------------------------------
#define BIT_SET(aByte, aBit)    ((aByte) = (uint8)((aByte) | (0x01 << (aBit))))
//----------------------------------------------------------------------------------------------------------------------
#define BIT_CLR(aByte, aBit)    ((aByte) = (aByte) & (~(0x01 << (aBit))))
//----------------------------------------------------------------------------------------------------------------------
#define BIT_TEST(aByte, aBit)   (((aByte) & (0x01 << (aBit))) > 0)
//----------------------------------------------------------------------------------------------------------------------
#define MAX(aValue, bValue)     (((aValue) > (bValue)) ? (aValue):(bValue))
//----------------------------------------------------------------------------------------------------------------------
#define MIN(aValue, bValue)     (((aValue) < (bValue)) ? (aValue):(bValue))
//----------------------------------------------------------------------------------------------------------------------
#define SWAP(aValue, bValue)    (                           \
                                    (aValue) ^= (bValue);   \
                                    (bValue) ^= (aValue);   \
                                    (aValue) ^= (bValue)    \
                                )
//----------------------------------------------------------------------------------------------------------------------
#define ABS(aValue)             (((aValue) < 0) ? -(aValue) : (aValue))
//----------------------------------------------------------------------------------------------------------------------
#define IS_EVEN(aValue)         (((aValue) & 0x01) == 0)
//----------------------------------------------------------------------------------------------------------------------
#define IS_ODD(aValue)          (((aValue) & 0x01) != 0)
//----------------------------------------------------------------------------------------------------------------------
#define UNDEF(aValue)           ((void)(aValue))
//----------------------------------------------------------------------------------------------------------------------

#endif /* STD_TYPES_H_ */
